export async function seedBuiltInThemes(): Promise<void> {
  const allExisting = await db.select({ id: themes.id, name: themes.name, isBuiltIn: themes.isBuiltIn }).from(themes);
  const existingByName = new Map(allExisting.map(t => [t.name, t]));

  let inserted = 0;
  let upgraded = 0;
  for (const theme of BUILT_IN_THEMES) {
    const existing = existingByName.get(theme.name);
    if (existing && existing.isBuiltIn) continue;

    if (existing && !existing.isBuiltIn) {
      await db.update(themes).set({
        isBuiltIn: true,
        light: theme.light,
        dark: theme.dark,
        fonts: theme.fonts,
      }).where(eq(themes.id, existing.id));
      upgraded++;
    } else {
      await db.insert(themes).values({
        name: theme.name,
        isBuiltIn: true,
        light: theme.light,
        dark: theme.dark,
        fonts: theme.fonts,
      });
      inserted++;
    }
  }
  if (inserted > 0 || upgraded > 0) {
    console.log(`[seed] Built-in themes: ${inserted} inserted, ${upgraded} upgraded`);
  }
}
